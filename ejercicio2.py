n1 = int(input("esriba el valor de n1: "))
n2 = int(input("escriba el valor de n2: "))
if n1 == n2:
 print(f'el {n1} y el {n2} son iguales!')
elif n1 > n2:
 print(f'el {n1} es mayor!')
else:
 print(f'el {n2} es mayor!')